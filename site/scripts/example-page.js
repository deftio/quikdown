/*
 * example-page.js — shared mount logic for example pages.
 *
 * Each example page just needs:
 *   <div id="qd-nav-mount"></div>   <!-- before <main> -->
 *   <div id="qd-footer-mount"></div>  <!-- after </main> -->
 *   <script src="/quikdown/site/scripts/example-page.js" defer></script>
 *
 * This loads the shared nav and footer partials and then loads site.js
 * for the theme toggle, copy buttons, etc.
 */
(function () {
    'use strict';

    function mount() {
        const navEl = document.getElementById('qd-nav-mount');
        const footerEl = document.getElementById('qd-footer-mount');
        if (!navEl || !footerEl) return;

        // Fetch nav, footer, AND version.json in parallel. version.json is
        // generated by tools/buildSite.js on every build, so it's always
        // current — no hardcoded version strings.
        Promise.all([
            fetch('/quikdown/site/components/nav.html').then(r => r.text()),
            fetch('/quikdown/site/components/footer.html').then(r => r.text()),
            fetch('/quikdown/site/version.json').then(r => r.json()).catch(() => ({ version: '?' })),
        ]).then(([nav, footer, info]) => {
            const version = info.version || '?';
            const replacers = (s) => s
                .replace(/\{\{VERSION\}\}/g, version)
                .replace(/\{\{COVERAGE\}\}/g, info.coverage || '?')
                .replace(/\{\{SIZE_CORE\}\}/g, info.sizeCore || '?')
                .replace(/\{\{SIZE_BD\}\}/g,   info.sizeBd   || '?')
                .replace(/\{\{SIZE_EDIT\}\}/g, info.sizeEdit || '?');
            navEl.outerHTML    = replacers(nav);
            footerEl.outerHTML = replacers(footer);

            // Now load the shared site script for theme toggle + copy buttons
            const s = document.createElement('script');
            s.src = '/quikdown/site/scripts/site.js';
            document.body.appendChild(s);
        }).catch((e) => {
            console.error('example-page.js: failed to mount nav/footer', e);
        });
    }

    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', mount);
    } else {
        mount();
    }
})();
